def find_best_config(results: List[SDGConfigResult]) -> SDGConfigResult:
    return max(results, key=lambda r: r.overall_quality)


def find_fastest_config(results: List[SDGConfigResult]) -> SDGConfigResult:
    return max(results, key=lambda r: r.demos_per_hour)


def find_best_sr_predictor(results: List[SDGConfigResult]) -> SDGConfigResult:
    return max(results, key=lambda r: r.expected_sr_improvement)


def score_color(score: float) -> str:
    """Return a hex color: red (low) → yellow → green (high)."""
    if score >= 0.80:
        return "#4ade80"   # green-400
    elif score >= 0.65:
        return "#facc15"   # yellow-400
    elif score >= 0.50:
        return "#fb923c"   # orange-400
    else:
        return "#f87171"   # red-400


