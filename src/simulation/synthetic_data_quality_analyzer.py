@dataclass
class SDGConfigResult:
    config_id: str
    display_name: str
    description: str
    # Quality scores 0-1 for each dimension
    scores: Dict[str, float]
    # Throughput
    demos_per_hour: float
    # Estimated SR improvement (percentage points) when fine-tuning on this data
    expected_sr_improvement: float
    # Overall weighted quality score (computed)
    overall_quality: float = field(init=False)

    def __post_init__(self):
        self.overall_quality = sum(self.scores.values()) / len(self.scores)


