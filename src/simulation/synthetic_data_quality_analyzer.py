def to_json(results: List[SDGConfigResult]) -> dict:
    best  = find_best_config(results)
    fast  = find_fastest_config(results)
    sr_p  = find_best_sr_predictor(results)
    return {
        "summary": {
            "best_overall":    best.config_id,
            "fastest":         fast.config_id,
            "best_sr_predictor": sr_p.config_id,
            "recommendation":  {
                "production": "isaac_full_dr",
                "rapid_iteration": "genesis_tuned",
            },
        },
        "configs": [
            {
                "config_id":            r.config_id,
                "display_name":         r.display_name,
                "description":          r.description,
                "scores":               {k: round(v, 4) for k, v in r.scores.items()},
                "overall_quality":      round(r.overall_quality, 4),
                "demos_per_hour":       round(r.demos_per_hour, 1),
                "expected_sr_improvement_pp": round(r.expected_sr_improvement, 2),
            }
            for r in results
        ],
    }


# ---------------------------------------------------------------------------
# CLI
# ---------------------------------------------------------------------------

def parse_args(argv: Optional[List[str]] = None) -> argparse.Namespace:
    p = argparse.ArgumentParser(
        description="Analyze synthetic data quality for GR00T fine-tuning."
    )
    p.add_argument(
        "--mock",
        action="store_true",
        help="Use mock data instead of reading from disk.",
    )
    p.add_argument(
        "--output",
        default="/tmp/synthetic_data_quality_analyzer.html",
        help="Path for the HTML report (default: /tmp/synthetic_data_quality_analyzer.html).",
    )
    p.add_argument(
        "--json-output",
        default=None,
        help="Optional path to write JSON results.",
    )
    p.add_argument(
        "--seed",
        type=int,
        default=42,
        help="Random seed for mock noise (default: 42).",
    )
    p.add_argument(
        "--no-html",
        action="store_true",
        help="Skip HTML generation (console + JSON only).",
    )
    return p.parse_args(argv)


def main(argv: Optional[List[str]] = None) -> int:
    args = parse_args(argv)

    # Data source
    if args.mock or True:  # currently only mock mode is implemented
        results = generate_mock_results(seed=args.seed)
    else:
        print("ERROR: Only --mock mode is currently supported.", file=sys.stderr)
        return 1

    # Console
    print_console_table(results)

    # HTML
    if not args.no_html:
        generate_html(results, args.output)

    # JSON
    data = to_json(results)
    if args.json_output:
        Path(args.json_output).write_text(
            json.dumps(data, indent=2), encoding="utf-8"
        )
        print(f"JSON results written to: {args.json_output}")
    else:
        print("\nJSON summary:")
        print(json.dumps(data["summary"], indent=2))

    return 0


if __name__ == "__main__":
    sys.exit(main())
