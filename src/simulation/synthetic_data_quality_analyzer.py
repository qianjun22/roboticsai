def generate_mock_results(seed: int = 42) -> List[SDGConfigResult]:
    rng = random.Random(seed)
    results = []
    for cid, base_scores in _CANONICAL_SCORES.items():
        # Add small noise so charts look measured, not rounded
        scores = {
            dim: max(0.0, min(1.0, v + rng.uniform(-0.015, 0.015)))
            for dim, v in base_scores.items()
        }
        results.append(SDGConfigResult(
            config_id=cid,
            display_name=_CONFIG_DISPLAY_NAMES[cid],
            description=_CONFIG_DESCRIPTIONS[cid],
            scores=scores,
            demos_per_hour=_CANONICAL_DEMOS_PER_HOUR[cid]
                           + rng.uniform(-20, 20),
            expected_sr_improvement=_CANONICAL_SR_IMPROVEMENT[cid]
                                    + rng.uniform(-0.3, 0.3),
        ))
    return results


