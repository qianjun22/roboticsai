def _fmt(v: float, decimals: int = 4) -> str:
    return f"{v:.{decimals}f}"


# Radar / Spider chart -------------------------------------------------------

CONFIG_COLORS = {
    "genesis_default": "#60a5fa",   # blue-400
    "genesis_tuned":   "#34d399",   # emerald-400
    "isaac_basic":     "#f472b6",   # pink-400
    "isaac_full_dr":   "#fbbf24",   # amber-400
}


def _polar_to_xy(cx: float, cy: float, r: float, angle_rad: float) -> Tuple[float, float]:
    return cx + r * math.sin(angle_rad), cy - r * math.cos(angle_rad)


def _radar_polygon(cx: float, cy: float, r: float, scores: List[float],
                    n: int) -> str:
    pts = []
    for i, s in enumerate(scores):
        angle = 2 * math.pi * i / n
        x, y = _polar_to_xy(cx, cy, r * s, angle)
        pts.append(f"{_fmt(x)},{_fmt(y)}")
    return " ".join(pts)


def svg_radar(results: List[SDGConfigResult], width: int = 520,
              height: int = 520) -> str:
    n = len(QUALITY_DIMENSIONS)
    cx, cy = width / 2, height / 2
    r_max = min(cx, cy) - 80
    spokes_per_ring = 5
    rings = [i / spokes_per_ring for i in range(1, spokes_per_ring + 1)]

    lines = [
        f'<svg xmlns="http://www.w3.org/2000/svg" '
        f'width="{width}" height="{height}" '
        f'viewBox="0 0 {width} {height}">'
    ]

    # Background rings
    for ring_frac in rings:
        pts = " ".join(
            f"{_fmt(_polar_to_xy(cx, cy, r_max * ring_frac, 2*math.pi*i/n)[0])},"
            f"{_fmt(_polar_to_xy(cx, cy, r_max * ring_frac, 2*math.pi*i/n)[1])}"
            for i in range(n)
        )
        lines.append(
            f'<polygon points="{pts}" fill="none" '
            f'stroke="#374151" stroke-width="1"/>'
        )

    # Spoke lines
    for i in range(n):
        angle = 2 * math.pi * i / n
        x, y = _polar_to_xy(cx, cy, r_max, angle)
        lines.append(
            f'<line x1="{_fmt(cx)}" y1="{_fmt(cy)}" '
            f'x2="{_fmt(x)}" y2="{_fmt(y)}" '
            f'stroke="#4b5563" stroke-width="1"/>'
        )

    # Ring value labels (0.2 .. 1.0)
    for ring_frac in rings:
        x, y = _polar_to_xy(cx, cy, r_max * ring_frac, 0)
        lines.append(
            f'<text x="{_fmt(x + 4)}" y="{_fmt(y)}" '
            f'fill="#9ca3af" font-size="10" '
            f'dominant-baseline="middle">{ring_frac:.1f}</text>'
        )

    # Data polygons (each config)
    for r_result in results:
        scores = [r_result.scores[d] for d in QUALITY_DIMENSIONS]
        pts = _radar_polygon(cx, cy, r_max, scores, n)
        color = CONFIG_COLORS[r_result.config_id]
        lines.append(
            f'<polygon points="{pts}" fill="{color}" '
            f'fill-opacity="0.15" stroke="{color}" '
            f'stroke-width="2"/>'
        )
        # Dot markers
        for i, s in enumerate(scores):
            angle = 2 * math.pi * i / n
            x, y = _polar_to_xy(cx, cy, r_max * s, angle)
            lines.append(
                f'<circle cx="{_fmt(x)}" cy="{_fmt(y)}" r="4" '
                f'fill="{color}" stroke="#111827" stroke-width="1"/>'
            )

    # Axis labels
    label_offset = 50
    for i, dim in enumerate(QUALITY_DIMENSIONS):
        angle = 2 * math.pi * i / n
        x, y = _polar_to_xy(cx, cy, r_max + label_offset, angle)
        label = DIMENSION_LABELS[dim].replace("\n", " / ")
        lines.append(
            f'<text x="{_fmt(x)}" y="{_fmt(y)}" '
            f'fill="#e5e7eb" font-size="11" font-weight="bold" '
            f'text-anchor="middle" dominant-baseline="middle">'
            f'{label}</text>'
        )

    lines.append("</svg>")
    return "\n".join(lines)


# Bar chart (demos/hour) -----------------------------------------------------

def svg_bar_chart(results: List[SDGConfigResult], width: int = 560,
                  height: int = 280) -> str:
    pad_left, pad_right, pad_top, pad_bot = 80, 30, 20, 50
    chart_w = width - pad_left - pad_right
    chart_h = height - pad_top - pad_bot

    max_val = max(r.demos_per_hour for r in results) * 1.15
    n = len(results)
    bar_w = chart_w / (n * 1.5)
    gap = chart_w / n

    lines = [
        f'<svg xmlns="http://www.w3.org/2000/svg" '
        f'width="{width}" height="{height}" '
        f'viewBox="0 0 {width} {height}">'
    ]

    # Axes
    x0, y0 = pad_left, pad_top
    x1 = pad_left + chart_w
    y1 = pad_top + chart_h
    lines.append(
        f'<line x1="{x0}" y1="{y0}" x2="{x0}" y2="{y1}" '
        f'stroke="#6b7280" stroke-width="1"/>'
    )
    lines.append(
        f'<line x1="{x0}" y1="{y1}" x2="{x1}" y2="{y1}" '
        f'stroke="#6b7280" stroke-width="1"/>'
    )

    # Y-axis gridlines
    for frac in [0.25, 0.5, 0.75, 1.0]:
        gy = y1 - chart_h * frac
        gv = max_val * frac
        lines.append(
            f'<line x1="{x0}" y1="{_fmt(gy)}" x2="{x1}" y2="{_fmt(gy)}" '
            f'stroke="#374151" stroke-dasharray="4,3" stroke-width="1"/>'
        )
        lines.append(
            f'<text x="{x0 - 6}" y="{_fmt(gy)}" fill="#9ca3af" '
            f'font-size="10" text-anchor="end" dominant-baseline="middle">'
            f'{gv:.0f}</text>'
        )

    # Bars
    for i, r_result in enumerate(results):
        bar_x = x0 + i * gap + (gap - bar_w) / 2
        bar_h = (r_result.demos_per_hour / max_val) * chart_h
        bar_y = y1 - bar_h
        color = CONFIG_COLORS[r_result.config_id]
        lines.append(
            f'<rect x="{_fmt(bar_x)}" y="{_fmt(bar_y)}" '
            f'width="{_fmt(bar_w)}" height="{_fmt(bar_h)}" '
            f'fill="{color}" fill-opacity="0.85" rx="3"/>'
        )
        # Value label
        lines.append(
            f'<text x="{_fmt(bar_x + bar_w / 2)}" y="{_fmt(bar_y - 6)}" '
            f'fill="{color}" font-size="11" font-weight="bold" '
            f'text-anchor="middle">'
            f'{r_result.demos_per_hour:.0f}</text>'
        )
        # X label
        lines.append(
            f'<text x="{_fmt(bar_x + bar_w / 2)}" y="{y1 + 16}" '
            f'fill="#d1d5db" font-size="11" text-anchor="middle">'
            f'{r_result.display_name}</text>'
        )

    # Y-axis title
    lines.append(
        f'<text x="{pad_left - 55}" y="{_fmt(pad_top + chart_h / 2)}" '
        f'fill="#9ca3af" font-size="11" text-anchor="middle" '
        f'transform="rotate(-90 {pad_left - 55} {_fmt(pad_top + chart_h / 2)})">'
        f'Demos / Hour</text>'
    )

    lines.append("</svg>")
    return "\n".join(lines)


# Scatter plot (quality vs SR improvement) -----------------------------------

def svg_scatter(results: List[SDGConfigResult], width: int = 520,
                height: int = 360) -> str:
    pad_left, pad_right, pad_top, pad_bot = 65, 30, 20, 55

    chart_w = width - pad_left - pad_right
    chart_h = height - pad_top - pad_bot

    x_vals = [r.overall_quality for r in results]
    y_vals = [r.expected_sr_improvement for r in results]

    x_min, x_max = min(x_vals) - 0.05, max(x_vals) + 0.05
    y_min, y_max = 0.0, max(y_vals) * 1.2

    def to_px(qv: float, sr: float) -> Tuple[float, float]:
        px = pad_left + (qv - x_min) / (x_max - x_min) * chart_w
        py = pad_top + chart_h - (sr - y_min) / (y_max - y_min) * chart_h
        return px, py

    x0, y0 = pad_left, pad_top
    x1 = pad_left + chart_w
    y1 = pad_top + chart_h

    lines = [
        f'<svg xmlns="http://www.w3.org/2000/svg" '
        f'width="{width}" height="{height}" '
        f'viewBox="0 0 {width} {height}">'
    ]

    # Grid
    for frac in [0.25, 0.5, 0.75, 1.0]:
        gy = y1 - chart_h * frac
        sr_v = y_min + (y_max - y_min) * frac
        lines.append(
            f'<line x1="{x0}" y1="{_fmt(gy)}" x2="{x1}" y2="{_fmt(gy)}" '
            f'stroke="#374151" stroke-dasharray="4,3" stroke-width="1"/>'
        )
        lines.append(
            f'<text x="{x0 - 6}" y="{_fmt(gy)}" fill="#9ca3af" '
            f'font-size="10" text-anchor="end" dominant-baseline="middle">'
            f'+{sr_v:.1f}pp</text>'
        )
    for frac in [0.0, 0.25, 0.5, 0.75, 1.0]:
        gx = x0 + chart_w * frac
        qv = x_min + (x_max - x_min) * frac
        lines.append(
            f'<line x1="{_fmt(gx)}" y1="{y0}" x2="{_fmt(gx)}" y2="{y1}" '
            f'stroke="#374151" stroke-dasharray="4,3" stroke-width="1"/>'
        )
        lines.append(
            f'<text x="{_fmt(gx)}" y="{y1 + 15}" fill="#9ca3af" '
            f'font-size="10" text-anchor="middle">{qv:.2f}</text>'
        )

    # Axes
    lines.append(
        f'<line x1="{x0}" y1="{y0}" x2="{x0}" y2="{y1}" '
        f'stroke="#6b7280" stroke-width="1"/>'
    )
    lines.append(
        f'<line x1="{x0}" y1="{y1}" x2="{x1}" y2="{y1}" '
        f'stroke="#6b7280" stroke-width="1"/>'
    )

    # Pareto frontier (dashed line through Pareto-optimal points)
    sorted_r = sorted(results, key=lambda r: r.overall_quality)
    pareto_pts: List[Tuple[float, float]] = []
    max_sr_so_far = -1.0
    for r in sorted_r:
        if r.expected_sr_improvement > max_sr_so_far:
            max_sr_so_far = r.expected_sr_improvement
            pareto_pts.append(to_px(r.overall_quality, r.expected_sr_improvement))
    if len(pareto_pts) >= 2:
        path_d = "M " + " L ".join(
            f"{_fmt(px)},{_fmt(py)}" for px, py in pareto_pts
        )
        lines.append(
            f'<path d="{path_d}" fill="none" stroke="#6b7280" '
            f'stroke-width="1.5" stroke-dasharray="6,4"/>'
        )

    # Points
    for r_result in results:
        px, py = to_px(r_result.overall_quality, r_result.expected_sr_improvement)
        color = CONFIG_COLORS[r_result.config_id]
        lines.append(
            f'<circle cx="{_fmt(px)}" cy="{_fmt(py)}" r="8" '
            f'fill="{color}" fill-opacity="0.85" '
            f'stroke="#111827" stroke-width="1.5"/>'
        )
        label_x = px + 11
        label_y = py - 4
        lines.append(
            f'<text x="{_fmt(label_x)}" y="{_fmt(label_y)}" '
            f'fill="{color}" font-size="11" font-weight="bold">'
            f'{r_result.display_name}</text>'
        )

    # Axis titles
    lines.append(
        f'<text x="{_fmt(x0 + chart_w / 2)}" y="{y1 + 38}" '
        f'fill="#9ca3af" font-size="11" text-anchor="middle">'
        f'Overall Quality Score</text>'
    )
    lines.append(
        f'<text x="{x0 - 50}" y="{_fmt(y0 + chart_h / 2)}" '
        f'fill="#9ca3af" font-size="11" text-anchor="middle" '
        f'transform="rotate(-90 {x0 - 50} {_fmt(y0 + chart_h / 2)})">'
        f'Expected SR Improvement</text>'
    )

    lines.append("</svg>")
    return "\n".join(lines)


# Legend SVG -----------------------------------------------------------------

def svg_legend(results: List[SDGConfigResult], width: int = 520) -> str:
    item_w = width // len(results)
    h = 32
    lines = [
        f'<svg xmlns="http://www.w3.org/2000/svg" '
        f'width="{width}" height="{h}" '
        f'viewBox="0 0 {width} {h}">'
    ]
    for i, r in enumerate(results):
        color = CONFIG_COLORS[r.config_id]
        x = i * item_w + 12
        lines.append(
            f'<rect x="{x}" y="10" width="14" height="14" '
            f'fill="{color}" rx="2"/>'
        )
        lines.append(
            f'<text x="{x + 19}" y="21" fill="#e5e7eb" '
            f'font-size="12">{r.display_name}</text>'
        )
    lines.append("</svg>")
    return "\n".join(lines)


